/* ── 在帧缓冲中画一个字符 ── */
void font_draw_char(uint8_t *buf, int x, int y,
                    char c, int buf_w, int inverted)
{
    if (c < 32 || c > 126) return;
    int idx = c - 32;
    if (idx >= (int)(sizeof(font8x16) / sizeof(font8x16[0]))) return;
    const uint8_t *glyph = font8x16[idx];

    for (int row = 0; row < 16; row++) {
        uint8_t line = glyph[row];
        for (int col = 0; col < 8; col++) {
            int px = x + col;
            int py = y + row;
            if (px < 0 || px >= buf_w) continue;
            if (py < 0 || py >= 480) continue;

            int byte_idx = py * (buf_w / 8) + (px / 8);
            int bit_idx  = 7 - (px % 8);
            int pixel    = (line >> (7 - col)) & 1;

            if (inverted) pixel = !pixel;
            if (pixel)
                buf[byte_idx] &= ~(1 << bit_idx);  // 黑色
            else
                buf[byte_idx] |= (1 << bit_idx);   // 白色
        }
    }
}

/* ── 在帧缓冲中画字符串 ── */
void font_draw_string(uint8_t *buf, int x, int y,
                      const char *str, int buf_w, int inverted)
{
    int cx = x;
    while (*str) {
        if (*str == '\n') {
            cx = x;
            y += 16;
        } else {
            font_draw_char(buf, cx, y, *str, buf_w, inverted);
            cx += 8;
        }
        str++;
    }
}
